class Defaultval 
{
	int a;
	float b;
	char  c;
	boolean d;
	//for instatnce varaiables jvm will assign default values
	public static void main(String[] args) 
	{
		Defaultval t = new Defaultval();
		System.out.println(t.a);
		System.out.println(t.b);
		System.out.println(t.c);
		System.out.println(t.d);
	}
}
